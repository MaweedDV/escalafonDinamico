                  <div class="block-content">
                    <p style="text-align: justify;">
                        El Escalafón de Mérito es el ordenamiento jerárquico anual de los funcionarios municipales,
                        elaborado conforme a los resultados de sus calificaciones de desempeño, de acuerdo con lo
                        dispuesto en la Ley N° 18.883, Estatuto Administrativo para Funcionarios Municipales.<br>
                        Su propósito es construir una herramienta técnica, objetiva y transparente que permita regular la carrera funcionaria, en los procesos de ascenso, promoción y permanencía dentro de la Municipalidad.<br>
                        El factor principal para el ordenamiento del Escalafón de Mérito son, en primera instancia,  las calificaciones de desempeño, y en el caso de existir empates, su ubicación se determina por antigüedad, aplicada según el siguiente
                        orden de prioridad: <br><br>
                        1. Antigüedad en el cargo actual. <br>
                        2. Antigüedad en el grado.<br>
                        3. Antigüedad en el municipio.<br>
                        4. Antigüedad en la Administración Pública.<br><br>
                        El escalafón tendrá vigencia desde el primero de Enero de cada año y tiene una duración de 12 meses.<br>
                        Para mayor información se recomienda revisar los artículos sobre calificación, artículos 29 al 50 de la Ley N° 18.883, “Estatuto Administrativo para Funcionarios
                        Municipales”.
